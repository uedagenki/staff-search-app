  // すべての職種を取得
  static List<String> getAllJobTitles() {
    final allJobs = <String>[];
    categories.forEach((category, jobs) {
      allJobs.addAll(jobs);
    });
    return allJobs;
  }

  // すべてのカテゴリー名を取得
  static List<String> getCategoryNames() {
    return categories.keys.toList();
  }

  // カテゴリーから職種を取得
  static List<String> getJobsByCategory(String category) {
    return categories[category] ?? [];
  }

  // 職種からカテゴリーを取得
  static String? getCategoryByJob(String jobTitle) {
    for (var entry in categories.entries) {
      if (entry.value.contains(jobTitle)) {
        return entry.key;
      }
    }
    return null;
  }
